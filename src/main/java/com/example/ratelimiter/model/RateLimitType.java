package com.example.ratelimiter.model;

/**
 * 限流算法类型
 *
 * @author Claude
 */
public enum RateLimitType {

    /**
     * 固定窗口计数器算法
     *
     * 原理：
     * - 将时间划分为固定大小的窗口（如1分钟）
     * - 在每个窗口内统计请求次数
     * - 当窗口内请求数超过阈值时，拒绝请求
     *
     * 优点：实现简单，内存占用低
     * 缺点：存在临界问题（窗口边界流量突刺）
     */
    FIXED_WINDOW,

    /**
     * 滑动窗口计数器算法
     *
     * 原理：
     * - 将时间窗口划分为多个小格子
     * - 每次请求记录时间戳
     * - 统计当前时间往前推N秒内的请求数
     *
     * 优点：解决固定窗口的临界问题，更平滑
     * 缺点：需要记录每个请求的时间戳，内存占用较高
     */
    SLIDING_WINDOW,

    /**
     * 令牌桶算法
     *
     * 原理：
     * - 系统以恒定速率向桶中放入令牌
     * - 请求需要从桶中获取令牌才能通过
     * - 桶有最大容量，满了之后令牌会溢出
     *
     * 优点：可以应对突发流量，灵活性高
     * 缺点：实现相对复杂
     */
    TOKEN_BUCKET,

    /**
     * 漏桶算法
     *
     * 原理：
     * - 请求像水一样流入漏桶
     * - 漏桶以恒定速率流出请求
     * - 桶满时新请求会溢出被拒绝
     *
     * 优点：可以强制限制数据传输速率，流量平滑
     * 缺点：无法应对突发流量
     */
    LEAKY_BUCKET
}
